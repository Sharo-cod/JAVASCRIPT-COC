const month = "January"

switch (month) {
    case "January":
        console.log('January');        
        break;
    case "February":
        console.log('February');        
        break;
    case "March":
        console.log('March');        
        break;
    case "April":
        console.log('April');        
        break;


    default:
        console.log("Default case Match");
        break;
}